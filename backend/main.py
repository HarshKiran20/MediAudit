@app.get("/audit/history")
async def get_history(db: Session = Depends(get_db)):
    # This fetches all records from PostgreSQL, newest first
    records = db.query(models.AuditRecord).order_by(models.AuditRecord.timestamp.desc()).all()
    
    # We need to format the data slightly for the frontend
    formatted_history = []
    for r in records:
        # Calculate an average risk score from the JSON analysis for the table view
        rows = r.analysis.get('rows', [])
        avg_risk = sum(int(row.get('risk_score', 0)) for row in rows) // len(rows) if rows else 0
        
        formatted_history.append({
            "id": r.id,
            "filename": r.filename,
            "insurer_name": r.insurer_name,
            "verdict": r.verdict,
            "risk_score": avg_risk,
            "timestamp": r.timestamp.isoformat()
        })
    return formatted_history
@app.get("/api/policies")
async def get_policies():
    """Returns a list of available insurance policy JSONs for the frontend dropdown."""
    try:
        # Check if the directory exists to avoid crashes
        if not os.path.exists(POLICY_DIR):
            return {"policies": []}
            
        # List all .json files and strip the extension for the UI
        files = [f for f in os.listdir(POLICY_DIR) if f.endswith('.json')]
        policy_names = [f.replace('.json', '') for f in files]
        
        return {"policies": policy_names}
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))
    
@app.get("/audit/download/{audit_id}")
async def download_report(audit_id: int, db: Session = Depends(get_db)):
    # 1. Fetch data from DB
    audit = db.query(models.AuditRecord).filter(models.AuditRecord.id == audit_id).first()
    if not audit:
        raise HTTPException(status_code=404, detail="Audit not found")

    # 2. Format data for the PDF generator
    report_data = {
        "insurer_name": audit.insurer_name,
        "filename": audit.filename,
        "verdict": audit.verdict,
        "rows": audit.analysis.get('rows', []),
        "eligibility_summary": audit.analysis.get('eligibility', {}).get('summary', '')
    }

    # 3. Generate PDF bytes
    pdf_bytes = generate_pdf(report_data)

    # 4. Return as a file download
    headers = {'Content-Disposition': f'attachment; filename="MediAudit_Report_{audit_id}.pdf"'}
    return Response(content=pdf_bytes, headers=headers, media_type="application/pdf")