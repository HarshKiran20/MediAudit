@app.post("/audit/upload", response_model=schemas.AuditResponse)
async def process_bill(file: UploadFile = File(...)):
    """
    Endpoint 1: Uploads a bill, runs OCR, and returns extracted text.
    """
    if not file.content_type.startswith("image/") and file.filename.split('.')[-1] not in ['pdf']:
        raise HTTPException(status_code=400, detail="Invalid file type")
    
    contents = await file.read()
    try:
        from groq import Groq
        client = Groq(api_key=os.getenv("GROQ_API_KEY"))
        
        extracted_text = extract_text_from_bytes(contents, file.filename, client)
        return {"filename": file.filename, "extracted_text": extracted_text}
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

@app.post("/audit/cross-check")
async def cross_check(data: schemas.CrossCheckRequest, db: Session = Depends(get_db)):
    # 1. Load the policy content from the file based on the name sent by frontend
    policy_path = os.path.join(POLICY_DIR, f"{data.policy_name}.json")
    
    if not os.path.exists(policy_path):
        raise HTTPException(status_code=404, detail=f"Policy {data.policy_name} not found")
        
    with open(policy_path, 'r') as f:
        policy_content = f.read() # Load the JSON rules as a string

    # 2. Now run the audit using the loaded content
    try:
        result = audit_claim(
            bill_text=data.bill_text,
            policy_baseline_text=policy_content
        )
        
        verdict_str = result.get('eligibility', {}).get('verdict', 'Under Review')
        
        new_audit = models.AuditRecord(
            filename=data.filename,
            insurer_name=data.insurer_name,
            verdict=verdict_str,
            analysis=result  # Stores the full JSON analysis
        )
        db.add(new_audit)
        db.commit()      # Save to PostgreSQL
        db.refresh(new_audit) # This fetches the new ID generated by the DB
        
        # --- RETURN BOTH ID AND ANALYSIS ---
        return {
            "id": new_audit.id, 
            "analysis": result
        }
    except Exception as e:
        db.rollback() # Undo DB changes if something crashed
        import traceback
        print(traceback.format_exc())
        raise HTTPException(status_code=500, detail=str(e))
    
